function showRandomImage(param) {
    // Seleziona l'array corretto in base al parametro
    const images = imagesMap[param];
    console.log(images, param)
    // Verifica se l'array di immagini esiste e non Ã¨ vuoto
    if (images && images.length > 0) {
        const randomIndex = Math.floor(Math.random() * images.length);
        const randomImage = images[randomIndex];
        const imgElement = document.getElementById("drugImage");
        imgElement.src = randomImage;
        imgElement.style.display = "block";
    } else {
        console.error("Nessuna immagine trovata per il parametro:", param);
    }
}